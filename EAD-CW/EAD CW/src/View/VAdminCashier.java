   private void display(){
       try{
        CAdminCashier obj=new CAdminCashier();
        ResultSet rst=obj.getEmployeeDetails();
        
        DefaultTableModel model=new DefaultTableModel();
        
        model.addColumn("Cashier ID");
        model.addColumn("Job ID");
        model.addColumn("Name");
        model.addColumn("Telephone number");
        model.addColumn("User ID");
        
        
        while(rst.next()){
        
            String[] x=new String[5];
            x[0]=rst.getString(1);
            x[1]=rst.getString(2);
            x[2]=rst.getString(3);
            x[3]=rst.getString(4);
            x[4]=rst.getString(5);
            
            model.addRow(x);
        }
        
        tbl_cashierView.setModel(model);
        
        }
        catch(SQLException e){
            
        }
   }
   
   
