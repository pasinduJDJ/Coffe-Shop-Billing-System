    private void txt_removeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_removeKeyReleased
        String PATTERN="^[0-9]{0,5}$";
        Pattern Patt=Pattern.compile(PATTERN);
        Matcher match=Patt.matcher(txt_remove.getText());
        if(!match.matches()){
            error_cash_id.setText("* Integear value only");
        }
        else {
            error_cash_id.setText(null);
        }
    }//GEN-LAST:event_txt_removeKeyReleased

