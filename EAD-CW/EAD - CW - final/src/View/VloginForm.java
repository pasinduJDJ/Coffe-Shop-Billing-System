    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            if(txt_id.getText().isEmpty()==true){
                error_userId.setText("User name cannot be blank");
            }
            else if(txt_passwd.getText().isEmpty()==true){
                error_password.setText("Password cannot be blank");
            }
            else{
                String username,type;
                int password;
                type=cmb_type.getSelectedItem().toString();
                username=txt_id.getText();
                password=Integer.parseInt(txt_passwd.getText());
                
                ResultSet rst;
                
                CloginForm login1=new CloginForm();
                rst=login1.getLoginDetails(type,username,password);
            
                if(rst.next()){
                    if(cmb_type.getSelectedItem()=="Admin"){
                        VAdminDashBoard obj=new VAdminDashBoard();
                        obj.setVisible(true);
                        this.setVisible(false);
                    }
                    else if(cmb_type.getSelectedItem()=="Cashier"){
                        VCashierDashboard obj=new VCashierDashboard();
                        obj.setVisible(true);
                        this.setVisible(false);
                        obj.dt(username);
                        
                        
                    }
                    
                }
                else{
                    error_password.setText("Please Check user_id and password");
                }
            }
             
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage(),"DataBase servaer error !",JOptionPane.ERROR_MESSAGE);
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, e.getMessage(),"Error !",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

