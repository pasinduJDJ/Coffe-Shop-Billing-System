    private void txt_passwdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_passwdKeyReleased
         String PATTERN="^[0-9]{0,5}$";
        Pattern Patt=Pattern.compile(PATTERN);
        Matcher match=Patt.matcher(txt_passwd.getText());
        if(!match.matches()){
            error_password.setText("* Integear value only");
        }
        else {
            error_password.setText(null);
        }
        
            
    }//GEN-LAST:event_txt_passwdKeyReleased

    private void txt_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_idKeyReleased
        String PATTERN="^[a-zA-Z0-9]{0,5}$";
        Pattern Patt=Pattern.compile(PATTERN);
        Matcher match=Patt.matcher(txt_id.getText());
        if(!match.matches()){
            error_userId.setText("* Cannot Apply Special caracter");
        }
        else {
            error_userId.setText(null);
        }
    }//GEN-LAST:event_txt_idKeyReleased

