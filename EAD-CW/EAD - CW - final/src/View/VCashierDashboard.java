    private void bill(int length) {
      String numbers1 = "0123456789";
      String numbers2 = "0123456789";
      String numbers3 = "0123456789";
      String numbers = "0123456789";
      String combinedChars = numbers1 + numbers2 + numbers3 + numbers;
      Random random = new Random();
      char[] bill = new char[length];

      bill[0] = numbers1.charAt(random.nextInt(numbers1.length()));
      bill[1] = numbers2.charAt(random.nextInt(numbers2.length()));
      bill[2] = numbers3.charAt(random.nextInt(numbers3.length()));
      bill[3] = numbers.charAt(random.nextInt(numbers.length()));
   
      for(int i = 4; i< length ; i++) {
         bill[i] = combinedChars.charAt(random.nextInt(combinedChars.length()));
      }
      txt_bill_no.setText(String.valueOf(bill));
   }
   
