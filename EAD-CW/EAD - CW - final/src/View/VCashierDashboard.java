     //------------------------------------------------------Tot Bill -----------------------------------------------------------------------------------//
     
     
     public void gettotBill(){
         int dis_rate;
         double tot_bill,tot_amount;
        
        tot_amount=Integer.parseInt(txt_amount1.getText());
        dis_rate=Integer.parseInt(txt_disrate.getText());
        tot_bill=tot_amount-((tot_amount/100)*dis_rate);
        
        txt_amount.setText(String.valueOf(tot_bill));
        
     }
     
